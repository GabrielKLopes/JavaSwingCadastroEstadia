package javaSwingProject;

public class Responsavel {
	String nome;
	String cpf;
	String telefone;
	String email;
	String endereco;
	int idade;
	
	public Responsavel(String nome, String cpf, String telefone, String email, String endereco, int idade) {
		this.nome = nome;
		this.cpf = cpf;
		this.telefone = telefone;
		this.email = email;
		this.endereco = endereco;
		this.idade = idade;
	}
}
